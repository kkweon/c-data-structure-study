#define TEST_2D(expected, actual, size)                                \
  do {                                                                 \
    for (size_t i = 0; i < (size); ++i)                                \
      TEST_ASSERT_EQUAL_INT_ARRAY((expected)[i], (actual)[i], (size)); \
  } while (0);

#define FREE_2D(xs, size)                 \
  do {                                    \
    for (size_t i = 0; i < (size); ++i) { \
      free((xs)[i]);                      \
    }                                     \
    free((xs));                           \
  } while (0);

