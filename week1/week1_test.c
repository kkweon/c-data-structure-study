void tearDown() {}

void test_spiral_matrix_basic() {
  int** actual = spiral_matrix(5);

  int expected[][5] = {
      // clang-format off
      {1,  2,  3,  4,  5},
      {16, 17, 18, 19, 6},
      {15, 24, 25, 20, 7},
      {14, 23, 22, 21, 8},
      {13, 12, 11, 10, 9},
      // clang-format on
  };

  TEST_2D(expected, actual, 5);
  FREE_2D(actual, 5);
}

